function generateFortune(dateStr) {
  const dateNum = parseInt(dateStr.replace(/-/g, ''));
  const index = dateNum % fortunes.length;
  const sign = fortunes[index];
  return {
    date: dateStr,
    signId: sign.id,
    fortune: sign.level,
    title: sign.title,
    poem: sign.poem,
    interpretation: sign.meaning,
    totalSigns: fortunes.length,
    generatedAt: new Date().toISOString()
  };
}

// Generate and write to file
const now = new Date();
const y = now.getFullYear();
const m = String(now.getMonth() + 1).padStart(2, '0');
const d = String(now.getDate()).padStart(2, '0');
const dateStr = y + '-' + m + '-' + d;
const result = generateFortune(dateStr);
const json = JSON.stringify(result, null, 2);

// In GitHub Actions, write to fortune/latest.json
const outputPath = process.argv[2] || 'fortune/latest.json';
